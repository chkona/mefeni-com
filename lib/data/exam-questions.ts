export function getTopic(slug: string): ExamTopic | undefined {
  return examTopics.find((t) => t.slug === slug);
}

export function getSubtopic(
  topicSlug: string,
  subtopicSlug: string
): { topic: ExamTopic; subtopic: ExamSubtopic } | undefined {
  const topic = getTopic(topicSlug);
  if (!topic) return undefined;
  const subtopic = topic.subtopics.find((s) => s.slug === subtopicSlug);
  if (!subtopic) return undefined;
  return { topic, subtopic };
}

