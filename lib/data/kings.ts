export function getKingsByEra(era: EraKey): King[] {
  return kings.filter((k) => k.era === era).sort((a, b) => a.order - b.order);
}

export function getKing(slug: string): King | undefined {
  return kings.find((k) => k.slug === slug);
}

export function getEra(slug: string): EraKey {
  const king = getKing(slug);
  return king ? king.era : "other";
}

